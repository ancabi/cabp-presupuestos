public class Telefonos {
	
	private String telefono;

	/**
	 * @param telefono
	 */
	public Telefonos(String telefono) {
		this.telefono = telefono;
	}

	public String getTelefono() {
		return telefono;
	}

	@Override
	public String toString() {
		return telefono;
	}
	
	
	
	

}
